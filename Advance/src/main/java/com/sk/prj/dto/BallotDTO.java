public class BallotDTO {
	private String ballotid;
	private String iteminfo;
	private String bctractflag;
	private String bcontractid;
	private String regdtm;
	
	public String getBallotid() {
		return ballotid;
	}

	public void setBallotid(String ballotid) {
		this.ballotid = ballotid;
	}

	public String getIteminfo() {
		return iteminfo;
	}

	public void setIteminfo(String iteminfo) {
		this.iteminfo = iteminfo;
	}

	public String getBctractflag() {
		return bctractflag;
	}

	public void setBctractflag(String bctractflag) {
		this.bctractflag = bctractflag;
	}

	public String getBcontractid() {
		return bcontractid;
	}

	public void setBcontractid(String bcontractid) {
		this.bcontractid = bcontractid;
	}

	public String getRegdtm() {
		return regdtm;
	}

	public void setRegdtm(String regdtm) {
		this.regdtm = regdtm;
	}
	
	
}
