package com.sk.prj.dto;

public class BChainResidentDTO {
	private String id;
	private String seq;
	private String bacnt;
	private String validflag;
	private String createdtm;
	private String deletedtm;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getBacnt() {
		return bacnt;
	}
	public void setBacnt(String bacnt) {
		this.bacnt = bacnt;
	}
	public String getValidflag() {
		return validflag;
	}
	public void setValidflag(String validflag) {
		this.validflag = validflag;
	}
	public String getCreatedtm() {
		return createdtm;
	}
	public void setCreatedtm(String createdtm) {
		this.createdtm = createdtm;
	}
	public String getDeletedtm() {
		return deletedtm;
	}
	public void setDeletedtm(String deletedtm) {
		this.deletedtm = deletedtm;
	}
	
	
}
